package my.com.vic3.common.lib;

import java.util.HashMap;
import java.util.Map;
import com.fasterxml.jackson.annotation.JsonAnyGetter;
import com.fasterxml.jackson.annotation.JsonAnySetter;
import com.fasterxml.jackson.annotation.JsonIgnore;
import com.fasterxml.jackson.annotation.JsonInclude;
import org.apache.commons.lang.builder.EqualsBuilder;
import org.apache.commons.lang.builder.HashCodeBuilder;

@JsonInclude(JsonInclude.Include.NON_NULL)
/**
 * @author Fauzan
 * 
 * This class provides responseHeader which implements interface ModelResponse
 * 
 */
public class BasicResponse extends JsonProperty implements ModelResponse{

    private ResponseHeader responseHeader;
    @JsonIgnore
    private Map<String, Object> additionalProperties = new HashMap<String, Object>();


    public ResponseHeader getResponseHeader() {
        return responseHeader;
    }


    public void setResponseHeader(ResponseHeader responseHeader) {
        this.responseHeader = responseHeader;
    }


    @JsonAnyGetter
    public Map<String, Object> getAdditionalProperties() {
        return this.additionalProperties;
    }

    @JsonAnySetter
    public void setAdditionalProperty(String name, Object value) {
        this.additionalProperties.put(name, value);
    }

    @Override
    public int hashCode() {
        return new HashCodeBuilder().append(responseHeader).append(additionalProperties).toHashCode();
    }

    @Override
    public boolean equals(Object other) {
        if (other == this) {
            return true;
        }
        if ((other instanceof BasicResponse) == false) {
            return false;
        }
        BasicResponse rhs = ((BasicResponse) other);
        return new EqualsBuilder().append(responseHeader, rhs.responseHeader).append(additionalProperties, rhs.additionalProperties).isEquals();
    }

}
